package org.JavaMalu;

public class DoWhile38 {
    public static void main(String[] args) {
        int numero = 1;
        System.out.println("Números ímpares entre 1 e 20: ");
        do {
            System.out.println(numero);
            numero+=2;

        }while (numero <= 20);
        System.out.println("Fim da contagem!");
    }
}
